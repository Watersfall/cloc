function showHidePolicies()
{
    let div = document.getElementById("policies");
    if(div.style.display === "none")
    {
        div.style.display = "block";
    }
    else
    {
        div.style.display = "none";
    }
}

function showHideWorld()
{
    let div = document.getElementById("world");
    if(div.style.display === "none")
    {
        div.style.display = "block";
    }
    else
    {
        div.style.display = "none";
    }
}